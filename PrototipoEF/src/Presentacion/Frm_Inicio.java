    private void JBtnAccesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnAccesoActionPerformed
        SQLUsuario modSql = new SQLUsuario();
        Usuario mod = new Usuario();
        
        Date date = new Date(); // Creamos un objeto de tipo Date
        DateFormat fechaHora = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss"); // Creamos un formato para la fecha
    
        String pass = new String(JTxtPassword.getPassword());
        
        if (!JTxtUsuario.getText().equals("") && !pass.equals("")) { 
            
            String nuevoPass = Hash.sha1(pass);
            
            mod.setUsuario(JTxtUsuario.getText());
            mod.setPassword(nuevoPass);
            mod.setLast_session(fechaHora.format(date));//Obtenermos la fecha actual con el formato establecido
            
            if (modSql.login(mod)) {
                this.dispose();
                Frm_Contenedor frmMenu = new Frm_Contenedor(mod);
                frmMenu.setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
                limpiar();
            }
        } 
        else {
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");
        }
    }//GEN-LAST:event_JBtnAccesoActionPerformed

